// All drive items in a flat list, indexed by parent_id
const ALL_DRIVE_ITEMS: DbDriveItem[] = [
  ...ROOT_FOLDERS,
  ...WEDDINGS_CHILDREN,
  ...RACHEL_DAVID_FILES,
  ...VENDOR_FILES,
  ...TEMPLATE_FILES,
  ...MARKETING_FILES,
]

export function getDemoDriveItems(parentId: string | null): DbDriveItem[] {
  return ALL_DRIVE_ITEMS
    .filter((item) => item.parent_id === parentId)
    .sort((a, b) => {
      // Folders first, then by name
      if (a.type !== b.type) return a.type === 'folder' ? -1 : 1
      return a.name.localeCompare(b.name)
    })
}

export function getDemoDriveItem(id: string): DbDriveItem | undefined {
  return ALL_DRIVE_ITEMS.find((item) => item.id === id)
}

export function getDemoDriveFolderPath(folderId: string | null): DbDriveItem[] {
  if (!folderId) return []
  const path: DbDriveItem[] = []
  let currentId: string | null = folderId
  while (currentId) {
    const item = ALL_DRIVE_ITEMS.find((i) => i.id === currentId)
    if (!item) break
    path.unshift(item)
    currentId = item.parent_id
  }
  return path
}
